    private void jbtnCommitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCommitActionPerformed
        try{
            vMsj = "* Verifica lo siguiente: \n";
            if(this.jtxtNombreUser.getText().equals("")){
                vMsj = vMsj + TagHelper.getTag("frmUsuario.nameUser") + " \n";
                vBan = false;
            }
            if(this.jtxtCveUsuario.getText().equals("")){
                vMsj = vMsj + TagHelper.getTag("frmUsuario.cveUser") + " \n";
                vBan = false;
            }
            
            if (false == vBan){
                valForm.msjInfo(vMsj);
            }else{
                Usuario usuario = new Usuario();
                UsuarioLogic userLogic = new UsuarioLogic();
                usuario.setNombre(this.jtxtNombreUser.getText());
                usuario.setCve_usuario(this.jtxtCveUsuario.getText());
                usuario.setContrasena(this.jtxtCveUsuario.getText());
                userLogic.saveUser(usuario);
            }
        }catch(Exception ex){
            System.out.println("Error: " + ex.getMessage());
            System.out.println("Error2: " + ex.getLocalizedMessage());
        }
    }//GEN-LAST:event_jbtnCommitActionPerformed

